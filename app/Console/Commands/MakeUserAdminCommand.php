<?php

namespace App\Console\Commands;

use App\Models\User;
use Illuminate\Console\Command;

class MakeUserAdminCommand extends Command
{
    /**
     * The name and signature of the console command.
     *
     * @var string
     */
    protected $signature = 'user:make-admin {email : The email of the user to make admin}';

    /**
     * The console command description.
     *
     * @var string
     */
    protected $description = 'Make a user an administrator';

    /**
     * Execute the console command.
     */
    public function handle(): int
    {
        $email = $this->argument('email');

        $user = User::where('email', $email)->first();

        if (! $user) {
            $this->error("User with email '{$email}' not found.");

            return self::FAILURE;
        }

        if ($user->isAdmin()) {
            $this->info("User '{$user->name}' ({$email}) is already an administrator.");

            return self::SUCCESS;
        }

        $user->update(['is_admin' => true]);

        $this->info("User '{$user->name}' ({$email}) is now an administrator.");

        return self::SUCCESS;
    }
}
